    private void inicializarTextArea(){
        if (tipoVentana == 0) {
            int y = 0;
            for (int x = 0; x < JFrmStart.getListMaestros().size(); x++) {
                y = x + 1;
                this.textAreaShowTeacher.append(y + ". "
                        + JFrmStart.getListMaestros().get(x).getName()
                        + " " + JFrmStart.getListMaestros().get(x).getApellidoP() + "\n");
            }
        } else {
            
            int b = 0;
            for(int a=0;a<JFrmStart.getListMaterias().size();a++){
                b = a;
                if(JFrmStart.getListMaterias().get(a).isHaveTeacher()){
                    this.textAreaShowTeacher.append( b + ". " 
                            + JFrmStart.getListMaterias().get(a).getNombreAsignatura() + "\n");
                }
            }
        }
    }

