    private void buttonTerminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonTerminarMouseClicked
        
        noMaestro = Integer.parseInt(this.textNumMaestro.getText());
        if (tipoVentana == 0) {
            JFrmStart.getListMaterias().get(JFrmUnirMaestros.getIndice()).setHaveTeacher(true);
            JFrmStart.getListMaterias().get(JFrmUnirMaestros.getIndice()).setEducador(JFrmStart.getListMaestros().get(JFrmUnirMaestros.getIndice()));
            this.dispose();
            new JFrmUnirMaestros().setVisible(true);
            
        } else {
            
            Asignatura[] listaMaterias = JFrmStart.getListAlumnos().get(JFrmUnirAlumnos.getIndice()).getListaMaterias();
            listaMaterias[(listaMaterias.length)+1] = JFrmStart.getListMaterias().get(Integer.parseInt(this.textNumMaestro.getText()));
            JFrmStart.getListAlumnos().get(JFrmUnirAlumnos.getIndice()).setListaMaterias(listaMaterias);
            this.dispose();
            new JFrmUnirAlumnos().setVisible(true);
        }
    }//GEN-LAST:event_buttonTerminarMouseClicked

