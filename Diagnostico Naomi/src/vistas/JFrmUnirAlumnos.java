    private void tableVerAlumnosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableVerAlumnosMousePressed
       
        if (evt.getClickCount() == 2) {
            Point point = evt.getPoint();
            int row = tableVerAlumnos.rowAtPoint(point);//fila y posisici√≥n en el Array
            int column = tableVerAlumnos.columnAtPoint(point);//columna
            indice = row;
            this.dispose();
            JFrmMaestYMater.setTipoVentana(1);
            new JFrmMaestYMater().setVisible(true);
        }
    }//GEN-LAST:event_tableVerAlumnosMousePressed

    private void buttonTerminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonTerminarMouseClicked
         GenCSV csvAlumnos = new CsvAlumnos();
        
        String[][] info;
        
        info = new String[JFrmStart.getListAlumnos().size()][];
        
        for(int a=0;a<JFrmStart.getListAlumnos().size();a++){
            
            Asignatura[] materias = JFrmStart.getListAlumnos().get(a).getListaMaterias();
            
            info[a][0] = JFrmStart.getListAlumnos().get(a).getName() + " lleva la materia de "
                    + materias[0];
        }
        
        csvAlumnos.generaCSV(info);
    }//GEN-LAST:event_buttonTerminarMouseClicked

